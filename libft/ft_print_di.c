#include "libft.h"

int	ft_print_di(int n)
{
	int	i;

	i = 0;
	ft_putnbr_fd(n, 1);
	if (n <= 0)
		i++;
	while (n != 0)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
